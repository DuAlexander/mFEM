classdef HideHandle < handle
    %HANDLEHIDE A wrapper class causing methods of handle to be hidden.
    %
    % Syntax
    %   classdef MyHandleClass < HideHandle
    %
    % Description
    %   classdef MyHandleClass < HideHandle makes MyHandleClass a subclass
    %   of the handle class, but hides all the methods, thus when the
    %   documentation is created the handle based methods to not appear.
    %
    % See Also handle Mesh Element Node
    %----------------------------------------------------------------------
    %  mFEM: A Parallel, Object-Oriented MATLAB Finite Element Library
